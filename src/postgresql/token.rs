use super::keyword::Keyword;

#[derive(Debug, PartialEq)]
pub(crate) enum Token<'a> {
    String(&'a str),
    Number(&'a str),
    Ident(&'a str),
    Keyword(Keyword),
    Period,
    Comma,
    Div,
    Eq,
    Equal,
    Greater,
    GreaterOrEqual,
    Less,
    LessOrEqual,
    Plus,
    Sub,
    Mul,
    Tilde,
    At,
    Sharp,
    Mod,
    Caret,
    Ampersand,
    Pipe,
    QuestionMark,
    LParen,
    RParen,
    LBracket,
    RBracket,
    Eof,
    Colon,
}
